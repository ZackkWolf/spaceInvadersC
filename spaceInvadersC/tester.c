void swap(int* first, int* second) {
    int temp = *first;
    *first = *second;
    *second = temp;
}

void drawVerticalLine(int x, int y0, int y1, short int color) {
    if (y0 > y1) {
        swap(&y0, &y1);
    }

    for (int i = y0; i <= y1; ++i) {
        plotPixel(x, i, color);
    }
}

void drawLine(int x0, int y0, int x1, int y1, short int color) {
    bool isSteep = abs(y1 - y0) > abs(x1 - x0);

    if (isSteep) {
        swap(&x0, &y0);
        swap(&x1, &y1);
    }

    if (x0 > x1) {
        swap(&x0, &x1);
        swap(&y0, &y1);
    }

    int deltaX = x1 - x0;
    int deltaY = abs(y1 - y0);
    int error = -(deltaX / 2);
    int y = y0;
    int yStep;

    if (y0 < y1) { yStep = 1; }
    else { yStep = -1; }

    for (int x = x0; x <= x1; ++x) {
        if (isSteep) { plotPixel(y, x, color); }
        else { plotPixel(x, y, color); }

        error += deltaY;
        if (error >= 0) {
            y += yStep;
            error -= deltaX;
        }
    }
}

