void drawShot(volatile bool* shotFired, int* shotPositionX, int* shotPositionY, int* score,
    bool* eraseShot, int* redSplatPositionX, int* redSplatPositionY, int* redSplatFrames,
    bool* eraseRedSplat, int squids_x[11], int squids_y[11], bool squids_status[11],
    int bunnies_x[2][11], int bunnies_y[2][11], bool bunnies_status[2][11],
    int skulls_x[2][11], int skulls_y[2][11], bool skulls_status[2][11]) {

    if (*shotFired) {
        *shotFired = false;	// turn the signal low

        // initialize the shot
        shotColor = 0xFFFF;
        *shotPositionX = playerX + PLAYER_WIDTH / 2;
        *shotPositionY = playerY - 7;

        //printf("%d\n", pixelBufferStart);
    }

    // check if a shot is in the air
    if (shotColor != 0x0000) {
        // clear the old shot
        drawVerticalLine(*shotPositionX, *shotPositionY + 4, *shotPositionY + 7, 0x0000);

        // move the shot upwards if its not at the top of the screen
        // or erase the shot by drawing a red splat ontop of it at the top of the screen
        if (*shotPositionY > 10) {
            //check if shot hits enemies

            for (int i = 0; i < 11; ++i) {
                if (*shotPositionX > squids_x[i] &&
                    *shotPositionX < (squids_x[i] + 8) &&
                    *shotPositionY < (squids_y[i] + 8) &&
                    *shotPositionY > squids_y[i] &&
                    squids_status[i]) {

                    //printf("here\n");
                    shotColor = 0x0000;
                    *eraseShot = true;   //set this to true so the next frame erases the shot

                    //draw the red splat
                    *redSplatPositionX = *shotPositionX - 4;
                    *redSplatPositionY = *shotPositionY - 4;
                    drawRedSplat(*redSplatPositionX, *redSplatPositionY);
                    *redSplatFrames = 1;

                    squids_status[i] = false;

                    //up the score and display it on the HEX
                    (*score)++;
                    displayScoreOnHex3_0(*score);
                }
            }
            for (int j = 0; j < 2; ++j) {
                for (int i = 0; i < 11; ++i) {
                    if (*shotPositionX > bunnies_x[j][i] &&
                        *shotPositionX < (bunnies_x[j][i] + 11) &&
                        *shotPositionY < (bunnies_y[j][i] + 8) &&
                        *shotPositionY > bunnies_y[j][i] &&
                        bunnies_status[j][i]) {

                        //printf("here\n");
                        shotColor = 0x0000;
                        *eraseShot = true;   //set this to true so the next frame erases the shot

                        //draw the red splat
                        *redSplatPositionX = *shotPositionX - 4;
                        *redSplatPositionY = *shotPositionY - 4;
                        drawRedSplat(*redSplatPositionX, *redSplatPositionY);
                        *redSplatFrames = 1;

                        bunnies_status[j][i] = false;

                        //up the score and display it on the HEX
                        (*score)++;
                        displayScoreOnHex3_0(*score);
                    }
                }
            }
            for (int j = 0; j < 2; ++j) {
                for (int i = 0; i < 11; ++i) {
                    if (*shotPositionX > skulls_x[j][i] &&
                        *shotPositionX < (skulls_x[j][i] + 12) &&
                        *shotPositionY < (skulls_y[j][i] + 8) &&
                        *shotPositionY > skulls_y[j][i] &&
                        skulls_status[j][i]) {

                        //printf("here\n");
                        shotColor = 0x0000;
                        *eraseShot = true;   //set this to true so the next frame erases the shot

                        //draw the red splat
                        *redSplatPositionX = *shotPositionX - 4;
                        *redSplatPositionY = *shotPositionY - 4;
                        drawRedSplat(*redSplatPositionX, *redSplatPositionY);
                        *redSplatFrames = 1;

                        skulls_status[j][i] = false;

                        //up the score and display it on the HEX
                        (*score)++;
                        displayScoreOnHex3_0(*score);
                    }
                }
            }


            // update its position and draw the shot
            *shotPositionY -= 4;
            drawVerticalLine(*shotPositionX, *shotPositionY, *shotPositionY + 3, shotColor);
        }
        else {
            //erase the shot
            shotColor = 0x0000;
            *eraseShot = true;   //set this to true so the next frame erases the shot

            //draw the red splat
            *redSplatPositionX = *shotPositionX - 4;
            *redSplatPositionY = *shotPositionY;
            drawRedSplat(*redSplatPositionX, *redSplatPositionY);
            *redSplatFrames = 1;
        }
    }
    else if (*eraseShot) {   //enters this on the next frame to draw the red splat over the shot
        drawRedSplat(*redSplatPositionX, *redSplatPositionY);
        *eraseShot = false;
    }

    // check if the red splat is being displayed
    if (*redSplatFrames != 0) {
        //if its been displayed for 20 frames, erase it
        if (*redSplatFrames > 20) {
            *redSplatFrames = 0;
            drawBlack(*redSplatPositionX, *redSplatPositionY, 8, 8);
            *eraseRedSplat = true;   //set this to true for the next frame to erase the red splat
        }
        else {
            (*redSplatFrames)++;
        }
    }
    else if (*eraseRedSplat) {
        drawBlack(*redSplatPositionX, *redSplatPositionY, 8, 8);
        *eraseRedSplat = false;
    }
}


